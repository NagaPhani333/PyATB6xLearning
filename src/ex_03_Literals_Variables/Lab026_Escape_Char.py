# Escape Sequence
# \n -> new line - prints in next line
# \t -> tab provides a tab space
# \b -> backspace (1 char backspace)

print("Hello\nWorld")
print("Hello\tWorld")
print("Hello\bWorld")
print("Hello\rWorld")
print("Hello\fWorld")
print("Hello\aWorld")

# Python Escape Sequences Table
#Escape Sequence  |  Meaning
#-----------------+-----------------------------------------
#\n               |  Newline
#\t               |  Horizontal Tab
#\r               |  Carriage Return
#\b               |  Backspace
#\f               |  Formfeed
#\v               |  Vertical Tab
#\a               |  ASCII Bell (BEL)
#\e               |  Escape
#\'               |  Single Quote
#\"               |  Double Quote
#\\               |  Backslash
#\0               |  Null Character
#\N{name}         |  Unicode Character Database Named Lookup
#\uXXXX           |  Unicode Character (16-bit hex value)
#\UXXXXXXXX       |  Unicode Character (32-bit hex value)
#\xhh             |  Character with Hex Value hh
#\ooo             |  Character with Octal Value ooo
